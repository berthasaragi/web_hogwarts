        <h3 class="text-center mb-5">SEJARAH BERDIRINYA Hogwarts University</h3>
        <p style="text-align: justify;">Sejarah Hogwarts University

        <p style="text-align: justify;">Hogwarts University adalah lembaga pendidikan sihir tertua dan paling terkemuka di dunia wizarding. Berdiri tegak di atas bukit di tengah-tengah Hutan Terlarang, kastil megah Hogwarts telah menjadi rumah bagi para penyihir dan penyihir muda selama berabad-abad.

        <p style="text-align: justify;">
        <br>Abad ke-10 M: Pendirian Hogwarts
        Hogwarts University didirikan pada abad ke-10 M oleh empat penyihir hebat: Godric Gryffindor, Helga Hufflepuff, Rowena Ravenclaw, dan Salazar Slytherin. Mereka bekerja sama untuk menciptakan sekolah sihir yang akan menjadi tempat belajar dan berkembang bagi generasi-generasi penyihir yang akan datang.

        <br>Abad ke-11 M: Pembangunan Kastil Hogwarts
        Pada abad ke-11 M, kastil Hogwarts mulai dibangun di atas bukit yang strategis di dekat Danau Hogwarts. Pembangunan kastil memakan waktu bertahun-tahun, tetapi akhirnya, bangunan megah tersebut menjadi simbol keunggulan dan kebanggaan bagi komunitas wizarding.

        <br>Abad ke-12 M: Pembentukan Asrama
        Pada abad ke-12 M, empat pendiri Hogwarts memutuskan untuk membagi siswa-siswi menjadi empat asrama yang berbeda: Gryffindor, Hufflepuff, Ravenclaw, dan Slytherin. Setiap asrama dipimpin oleh salah satu pendiri, dan siswa-siswi ditempatkan berdasarkan kepribadian dan nilai-nilai yang mereka miliki.

        <br>Abad ke-13 M: Penyempurnaan Kurikulum
        Pada abad ke-13 M, kurikulum di Hogwarts diperluas untuk mencakup berbagai mata pelajaran sihir, termasuk transfigurasi, ramuan, bertahan hidup terhadap ilmu gelap, dan banyak lagi. Fasilitas-fasilitas baru juga dibangun, termasuk lapangan Quidditch dan perpustakaan yang luas.

        <br>Abad ke-20 M: Kepemimpinan Albus Dumbledore
        Selama abad ke-20 M, kepemimpinan Albus Dumbledore membawa Hogwarts ke era keemasannya. Sebagai Kepala Sekolah yang bijaksana dan kuat, Dumbledore membawa inovasi dalam pendidikan sihir sambil mempertahankan tradisi-tradisi klasik Hogwarts.

        <br>Abad ke-21 M: Masa Kini
        Hingga saat ini, Hogwarts University terus menjadi pusat keunggulan dalam pendidikan sihir. Dengan kepemimpinan Profesor Minerva McGonagall, sekolah ini terus menyediakan lingkungan belajar yang inspiratif, inklusif, dan mempersiapkan siswa-siswi untuk menghadapi tantangan dunia sihir yang terus berubah.

        <br>Sebagai salah satu institusi pendidikan tertua dan paling terhormat di dunia wizarding, Hogwarts University tetap menjadi simbol harapan, keberanian, dan pengetahuan yang memandu siswa-siswi dalam perjalanan mereka menuju masa depan yang cerah.</p>
